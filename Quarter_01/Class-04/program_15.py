#* ========= Negative Indexing with `pop()` ========= *#
#* ================================================== *#

# #               -4           -3       -2      -1
items = [ "Hara dhaniya", "Podina", "Dahi", "Andey" ]

print(items)
items.pop(-2) # remove "Dahi"   # items = [ "Hara dhaniya", "Podina", "Andey" ]
print(items)