#* ========= Adding Elements with `insert()` ======== *#
#* ================================================== *#

# #                0           1        2        3
items = [ "Hara dhaniya", "Podina", "Dahi", "Andey" ]

print(items)
items.insert(3, "Aalo") # add "Aalo"    # [ "Hara dhaniya", "Podina", "Dahi", "Aalo", "Andey" ]
print(items)