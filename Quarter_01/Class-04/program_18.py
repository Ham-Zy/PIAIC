#* ========== Removing Elements with `remove()` ===== *#
#* ================================================== *#

# #                0           1        2        3
items = [ "Hara dhaniya", "Podina", "Dahi", "Andey" ]

print(items)
items.remove("Dahi") # remove "Dahi"    # [ "Hara dhaniya", "Podina", "Aalo", "Andey" ]
print(items)